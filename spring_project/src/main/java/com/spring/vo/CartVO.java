package com.spring.vo;

public class CartVO {

	String wid, pid, pmphoto, ptitle, pkind1, pkind2;

	public String getWid() {
		return wid;
	}

	public void setWid(String wid) {
		this.wid = wid;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public String getPmphoto() {
		return pmphoto;
	}

	public void setPmphoto(String pmphoto) {
		this.pmphoto = pmphoto;
	}

	public String getPtitle() {
		return ptitle;
	}

	public void setPtitle(String ptitle) {
		this.ptitle = ptitle;
	}

	public String getPkind1() {
		return pkind1;
	}

	public void setPkind1(String pkind1) {
		this.pkind1 = pkind1;
	}

	public String getPkind2() {
		return pkind2;
	}

	public void setPkind2(String pkind2) {
		this.pkind2 = pkind2;
	}
}
